import java.util.Scanner;
public class arithmetic {
    public static void main(String[] args){
        Scanner a = new Scanner(System.in);
        int A = a.nextInt();
        int B = a.nextInt();
        System.out.println(A+B);
        System.out.println(A-B);
        System.out.println(A*B);
        System.out.println(A/B);
        a.close();

    }
    
}
